#include "partlib_base.h"
#include "Gem/State.h"

/////////////////////////////////////////////////////////
//
// partlib_base
//
/////////////////////////////////////////////////////////
// Constructor
//
/////////////////////////////////////////////////////////
partlib_base :: partlib_base() : m_tickTime(0)
{
}

/////////////////////////////////////////////////////////
// Destructor
//
/////////////////////////////////////////////////////////
partlib_base :: ~partlib_base()
{
}

/////////////////////////////////////////////////////////
// render
//
/////////////////////////////////////////////////////////
void partlib_base :: render(GemState *state)
{
  m_tickTime=50.;

  if(state) {
    state->get(GemState::_TIMING_TICK, m_tickTime);
  }

  renderParticles(state);
}
